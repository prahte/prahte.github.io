	<p>The competition pool is 50 meters by 10 lanes, with two movable bulkheads so that courses can be set up for 25 yards, 25 meters or 30 meters for water polo. It also features a movable floor which can be set from zero depth to seven feet, eight inches.</p>

	<p>The dive pool features one- and three-meter springboards plus one-, three-, five-, seven and a half- and 10-meter platforms, as well as a Spargar system that sends a mass of bubbles from the bottom of the tank to ease entry into the water.</p>
	
	<p>The original $21 million facility was funded entirely by the Atlanta Committee for the Olympic Games (ACOG). Construction on the facility began in July 1994 and was completed in time to host the VIIth Synchronized Swimming World Cup in August 1995. The newly-renovated enclosure of the GTAC is part of an over $45 million expansion to the CRC.</p>
	
	<p>There is full ventilation of the facility, including humidity control and air conditioning. The facility has indirect natural sunlight coming through high glass windows on one side.</p>

	<p>The facility includes men's and women's locker rooms, each containing 35 double-width lockers, with a team meeting room situated between the two.</p>
	
