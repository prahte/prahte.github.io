	<h5 style="padding-top: 20px; margin-top: 20px; border-top: 1px solid #c4c4c4;">Little Creek Horse Farm: <em>Ridding</em></h5>

	<p>The Little Creek Horse Farm is the riding site for Modern Pentathlon. The riding and running venues for the 2009 Modern Pentathlon World Cup II were to  be held at Little Creek Horse Farm. DeKalb County purchased in 2004 Little Creek Horse Farm. The farm covers 53 acres and is located at 2057 Lawrenceville Highway in Decatur, Georgia 30033.</p>

