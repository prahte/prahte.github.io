	<p>The Aquatic Center was the site of all swimming, diving and synchronized swimming competition as well as the swimming portion of the modern
	pentathlon during the Centennial Olympic Games in the summer of 1996. The Aquatic Center provides the ATLANTA 1996 and Georgia Tech with a world-class
	venue for aquatic sports and serves as an important element of the physical legacy of the Games. The 1,900-seat main stadium contains a
	competition pool and dive pool.</p>
	
