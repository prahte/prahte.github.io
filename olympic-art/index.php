	<h4 class="blue-text">About the Artist</h4>
	
	<div style="width: 455px;">
	<img src="/images/steveallen-portrait.jpg" width="125" height="120" border="0" alt="Steve R. ALLEN" class="float-left p-border"/>
	<h6>Steve R. Allen &bull; Olympic Artist</h6>
	<h6>U.S. Olympic Committee's Official<br />Olympic Artist Since 1998</h6>
	<h6>ATLANTA DITC's Artist-In-Residency Since 2003</h6>
	
	<p>Allen's talent was first showcased before a world audience when he was commissioned by the City of Atlanta to create <span class="italic">Uniting Colors of the
	World</span>, an eighty by twenty-eight feet mural. Allen's painting was Atlanta's official commemoration to the 1996 Centennial Olympic Games.</p>

	<p>In 2000, Allen's painting <span class="italic">On to Sydney</span> was presented during the 2000 Sydney Olympic Games on behalf of the city of Atlanta and the United
	States. In 2002, Allen was commissioned by the United States Olympic Committee (USOC) to create <span class="italic">Spirit of the Games</span> as the official commemorative
	piece of the 2002 Salt Lake City Olympic Games. In 2004, Allen created <span class="italic">Coming Full Circle (Return to Athens)</span> the official USOC artwork for the
	2004 Athens Olympic Games, and he also created <span class="italic">Unity and Peace Through Sport</span> now hanging in Athens' City Hall. This last painting was presented,
	during the Athens 2004 Olympic Games, to Athens Mayor Dora Bakoyannis by US Congresswoman Diane Watson. Allen's Olympic painting was the
	official poster image for the USOC for the 2006 Torino Olympic Games.</p>

	<p>In 2003, Allen became ATLANTA DITC's first Artist-In-Residency. In 2004, Allen created the art work for the <a href="http://www.dekalbclassic.us">Atlanta DeKalb International Prep Classic</a> award
	medals. During the <a href="http://www.ditc.us/olympic_journal/olympic-journal.html">2004 Athens Olympic Games</a>, Prince Albert II of Monaco, CEO Vernon Jones and Dr. Marc Daniel Gutekunst unveiled on August 23, 2004 ATLANTA DITC's first
	commissioned Olympic painting, <span class="italic">The <em>DITC from 1996 to the 2004 Games</em></span>. The painting was on display at the VISA Olympian Reunion Center for the
	duration of the 2004 Athens Olympic Games.</p>

	<p>The 2nd ATLANTA DITC Olympic painting, <span class="italic"><em>The DITC Olympian Legacy</em></span>, is also by Steve Allen. Allen donated to the ATLANTA DITC this painting valued at $365,000. To
	date this is the most generous individual gift ever made to the ATLANTA DITC. <span class="italic">The <em>DITC Olympian Legacy</em></span> was unveiled during the <a href="http://www.ditc.us/news-events/articles/article.php?id=6&year=2006">2006 Torino Olympic Games</a>	and on display February 9-26, 2006 at the VISA Olympian Reunion Center in Torino, Italy. <span class="italic">The <em>DITC Olympian Legacy</em></span>, features the flags of the ATLANTA DITC
	10 founding countries, ATLANTA DITC trained athletes Enefiok Udo-Obong who took Bronze at the 2004 Olympic Games, Olympic Champion Sergey Bubka who lends
	his name to the ATLANTA DITC Pole Vaulting Program, Silver Medallist Amadou Dia Ba from Senegal, pentathlete Robert Stull from the USA, 5 time Olympian and
	ATLANTA DITC Co-Founder Prince Albert of Monaco, DeKalb County CEO Vernon Jones, ATLANTA DITC Co-Chairs Ambassador Andrew Young, and Dr Marc Daniel Gutekunst.<br />
	<br />
	The 3rd ATLANTA DITC Olympic painting, 
	<em>A Bridge of Friendship from Atlanta to Beijing</em>, is also by Steve Allen. It was officially handed over to the Mayor of the City of Beijing and the Beijing Organizing Committee of the Olympic Games (BOCOG) on <a href="http://www.ditc.us/beijing/post.php?pID=24">August 26, 2008</a> during an official ceremony that took place at the Prince Jun Wang Fu Palace in Beijing, China site of the Beijing 2008 OLYMPIANS Reunion Center where the ATLANTA DITC had <a href="http://www.ditc.us/beijing/">3 permanent displays</a> for the duration of the BEIJING 2008 Olympic Games. </p>
	
