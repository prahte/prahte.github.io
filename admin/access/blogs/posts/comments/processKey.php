<?php

if($_POST) { 

	# ======================================================================
	# Canceling
	# ======================================================================
	if($_POST['submit'] == "Cancel") {
		unset($_SESSION['form']); unset($_SESSION['alert']);
		header("Location:index.php?pID=".$_REQUEST['pID']);
	}
		
	# ======================================================================
	# Delete
	# ======================================================================
	if($_POST['submit'] == "Delete") { require("p_deleteListing.php"); }
	
	# ======================================================================
	# Edit
	# ======================================================================
	if($_POST['submit'] == "Edit") { require("p_editListing.php"); }
	
	# ======================================================================
	# Post a Reply
	# ======================================================================
	if($_POST['submit'] == "Reply") { require("p_createListing.php"); }
	
	# ======================================================================
	# Save
	# ======================================================================
	if($_POST['submit'] == "Save" || $_POST['submit'] == "Save Edits") { require("p_saveListing.php"); }
	
} else {

	require("p_currentListings.php");

}
?>