	<p>The following day in a meeting between Prince Albert II of Monaco and Dr. Marc-Daniel Gutekunst the concept of the 1996 was established. However,
	  it took 6 years of hard work to make the 1996 concept a reality thanks to the partnership between FNT and DeKalb County. On May 6, 2002,
	  Ambassador Andrew Young, former Atlanta Mayor and Co-Chair of the 1996 Atlanta Olympic Games, inaugurated the 1996 in the presence of
	  representatives of 8 countries and officials from Georgia. On September 20, 2002, the 1996 was officially opened by its Co-Founder Prince Albert
	  of Monaco. ATLANTA 1996, The  Legacy Institution of the Atlanta 1996 Centennial Olympic Games. <br />
	  <br />
	  Since June 2002, the ATLANTA 1996 has been fully operational and has trained athletes from Europe, Africa and the Americas. Eight (8) training disciplines
	  are currently offered: <a href="/sports/cycling.php">Cycling</a>, <a href="http://www.ditc.us/sports/pentathlon.php">Modern Pentathlon</a> (Track &amp; Field, Swimming, Fencing, Shooting and Ridding), <a href="/sports/swimming.php">Swimming</a>, <a href="http://www.ditc.us/sports/team-handball.php">Team Handball</a>, <a href="/sports/tennis.php">Tennis</a>, and <a href="/sports/track-field.php">Track &amp; Field (Athletics)</a>. Since 2002, over 2,000 athletes from 53 nations trained and competed with ATLANTA 1996. ATLANTA 1996 trained athletes competed and achieved Olympic glory at the Athens 2004 and Beijing 2008 Games, and competed at the London 2012 Olympic Games.<br />
	  <br />
	  From 2002 to 2007, ATLANTA 1996 was known as the DeKalb International Training Center (DITC). When on June 2007 the DITC became USOC's designated training site for USA Team Handball's Men Team, it changed its name to ATLANTA DITC. On July 1, 2012, the ATLANTA DITC became ATLANTA 1996, and  Forging New Tomorrows (FNT) -- the partner  non-profit organization of ATLANTA 1996 -- became the <a href="www.atl96foundation.org">Atlanta 1996 Centennial Foundation</a> (ATL96 Foundation). </p>
  
