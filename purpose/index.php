	<p>The <span class="red-text">mission</span> of the ATLANTA 1996 is to provide first class training to enable participating athletes to compete in the US and internationally while
	  furthering their education to graduate with either a high school, a vocational school, or a university degree. Athletes in training at the ATLANTA 1996 prepare for the Olympic games and proudly represent their countries of origin and win Olympic Medals for their homeland.</p>
  
	<p>Upon completing their stay at the ATLANTA 1996, athletes will be returning to their home country with at least one medal (a high school or
	  college/university degree), and in the best of cases with two medals, a degree and an Olympic medal.</p>
  
	<p>The ATLANTA 1996 promotes peace and reconciliation, and fosters exchanges between people and nations while respecting the
	  socio-cultural and religious identity of each and every athlete in training and competing with us. Thus the ATLANTA 1996 mission statement: &quot;<strong>Peace &amp; Reconciliation Through Sports and Education (PARSE) </strong>.&quot; </p>
	  
