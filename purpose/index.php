	<p>On July 30, 1996, during the 26th Olympiad in Atlanta, Forging New Tomorrows (FNT), an international Atlanta based organization, together with
	  former Atlanta Mayor Maynard Jackson and Martin Luther King III hosted a reception for the National Olympic Committee of Burundi. At that time,
	  FNT announced that it was adopting the Burundi Olympic Team for the 1996 Atlanta Games. A few days later, on August 3, 1996, Burundi track athlete
	  V&eacute;nuste Niyongabo won a Gold medal in the 5,000 meter race. This was the first Olympic medal ever won by a citizen of Burundi, not to mention that
	  Burundi was participating in the Olympic Games for the first time. The medal was presented to Niyongabo by Prince Albert of Monaco, Member of the
	  International Olympic Committee (IOC).</p>
	  
